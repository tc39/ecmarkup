import type { BiblioEntry } from './Biblio';

import Spec from './Spec';
import * as utils from './utils';
import { CancellationToken } from 'prex';

export type { Spec, BiblioEntry };

export class Boilerplate {
  address?: string;
  copyright?: string;
  license?: string;
}

export type EcmarkupError = {
  ruleId: string;
  message: string;
  line: number;
  column: number;
  nodeType: string;
  source: string;
};

export interface Options {
  status?: 'proposal' | 'draft' | 'standard';
  version?: string;
  title?: string;
  shortname?: string;
  stage?: string | null;
  copyright?: boolean;
  date?: Date;
  location?: string;
  contributors?: string;
  toc?: boolean;
  oldToc?: boolean;
  lintSpec?: boolean;
  cssOut?: string;
  jsOut?: string;
  assets?: 'none' | 'inline' | 'external';
  outfile?: string;
  boilerplate?: Boilerplate;
  ecma262Biblio?: boolean;
  log?: (msg: string) => void;
  warn?: (err: EcmarkupError) => void;
}

export async function build(
  path: string,
  fetch: (path: string, token: CancellationToken) => PromiseLike<string>,
  opts?: Options,
  token = CancellationToken.none
): Promise<Spec> {
  const html = await fetch(path, token);
  const dom = utils.htmlToDom(html);
  const spec = new Spec(path, fetch, dom, opts, /*sourceText*/ html, token);
  await spec.build();
  return spec;
}
